#include <iostream>
using namespace std;

int main() {
    // Perulangan dengan FOR
    cout << "Perulangan FOR " << endl;

    // Perulangan dengan FOR untuk naik
    for (int i = 0; i < 5; ++i) {
        cout << "Nilai i: " << i << " Teknologi Informasi UMY" << endl;
    }
    cout << endl;

    // Perulangan dengan FOR untuk turun
    cout << "Perulangan FOR turun " << endl;
    for (int i = 5; i > 0; i--) {
        cout << "Nilai i: " << i << " Teknologi Informasi UMY" << endl;
    }
    cout << endl;

    return 0;
}
